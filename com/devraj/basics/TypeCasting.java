package com.devraj.basics;

public class TypeCasting {
    public static void main(String[] args) {
        // explicit typecasting
        double x = 10.7;
        int y = (int) x;
        System.out.println(y);

        //implicit typecasting
        int a = 10;
        double b = a;
        System.out.println(b);

    }
}
